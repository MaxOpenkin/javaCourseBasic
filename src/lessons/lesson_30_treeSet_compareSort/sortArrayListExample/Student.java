package lessons.lesson_30_treeSet_compareSort.sortArrayListExample;

public class Student {

    int roll;
    String name;

    public Student(int roll, String name) {
        this.roll = roll;
        this.name = name;
    }

    @Override
    public String toString() {
        return "StudentDemo{" +
                "roll=" + roll +
                ", name='" + name + '\'' +
                '}';
    }
}
