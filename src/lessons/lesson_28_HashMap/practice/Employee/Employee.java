package lessons.lesson_28_HashMap.practice.Employee;

public class Employee {

    private String id;
    private String nameEmployee;


    public Employee(String id, String nameEmployee) {
        this.id = id;
        this.nameEmployee = nameEmployee;
    }


    public String getId() {
        return id;
    }

    public String getName() {
        return nameEmployee;
    }

}
