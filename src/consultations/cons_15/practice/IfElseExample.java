package consultations.cons_15.practice;

public class IfElseExample {
    public static void main(String[] args) {
        int x = - 10;

        if (x < 0) {
            System.out.println("Число отрицательное");
        }
        if (x % 2 == 0) {
            System.out.println("Число четное");
        }

    }
}
